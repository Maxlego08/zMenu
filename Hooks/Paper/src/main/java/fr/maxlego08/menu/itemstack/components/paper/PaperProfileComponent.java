package fr.maxlego08.menu.itemstack.components.paper;

import fr.maxlego08.menu.api.context.BuildContext;
import fr.maxlego08.menu.api.itemstack.ItemComponent;
import fr.maxlego08.menu.api.utils.resolvable.Resolvable;
import fr.maxlego08.menu.api.utils.resolvable.bukkit.ResolvableProfileResolvable;
import io.papermc.paper.datacomponent.DataComponentTypes;
import org.bukkit.entity.Player;
import org.bukkit.inventory.ItemStack;
import org.jetbrains.annotations.NotNull;
import org.jetbrains.annotations.Nullable;

public class PaperProfileComponent extends ItemComponent {
    private final @Nullable ResolvableProfileResolvable resolvable;

    public PaperProfileComponent(@Nullable ResolvableProfileResolvable resolvable) {
        this.resolvable = resolvable;
    }

    @Override
    public void apply(@NotNull BuildContext context, @NotNull ItemStack itemStack, @Nullable Player player) {
        Resolvable.applyResolvable(context, this.resolvable, resolved -> itemStack.setData(DataComponentTypes.PROFILE, resolved));
    }
}
